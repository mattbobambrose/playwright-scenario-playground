const byId = new Map<number, Book>(books.map((b) => [b.id, b]));

export function findById(id: number): Book | undefined {
  return byId.get(id);
}

export function findByIds(ids: number[]): Book[] {
  return ids.map((id) => byId.get(id)).filter((b): b is Book => b !== undefined);
}
